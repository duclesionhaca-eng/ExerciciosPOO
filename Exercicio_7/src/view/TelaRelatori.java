        private int contarItens(String texto) {
        if (texto == null || texto.isEmpty()) return 0;
        // Conta as linhas que come√ßam com "‚Ä¢" (itens da lista)
        int count = 0;
        for (String linha : texto.split("\n")) {
            if (linha.trim().startsWith("‚Ä¢")) {
                count++;
            }
        }
        return count;
    }
    
