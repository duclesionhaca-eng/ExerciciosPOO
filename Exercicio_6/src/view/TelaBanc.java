     private void configurarComponentes() {
        // Configurar ComboBox
        jComboBoxConta.removeAllItems();
        jComboBoxConta.addItem("Conta Corrente");
        jComboBoxConta.addItem("Conta Poupança");
        
        // Configurar tabela - CORRIGINDO A ORDEM DAS COLUNAS
        configurarTabela();
        
        // Configurar labels
        lblNumContas.setText("Total: 0 contas");
        lblTaxas.setText("Taxas Totais: 0,00 MT");
        lblTaxas.setForeground(Color.BLUE);
    }
    
      private void configurarTabela() {
        // CORREÇÃO: A ordem das colunas deve ser igual ao TableModel
        tableModel = new DefaultTableModel(
            new Object[][]{}, 
            new String[]{"Taxa", "Tipo", "Num Conta", "Titular", "Saldo"} // Mesma ordem do designer
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        jTable1.setModel(tableModel);
    }

      private void atualizarTabela() {
        // Limpa a tabela
        tableModel.setRowCount(0);
        
        // Preenche com os dados - CORRIGINDO A ORDEM
        for (Conta conta : control.getContas()) {
            tableModel.addRow(new Object[]{
                String.format("%.2f", conta.calcularTaxa()) + " MT", // Taxa
                conta.getTipo(),                                    // Tipo
                conta.getNumeroConta(),                            // Num Conta
                conta.getTitular(),                                // Titular
                String.format("%.2f", conta.getSaldo()) + " MT"    // Saldo
            });
        }
        
        // Atualiza contador
        lblNumContas.setText("Total: " + control.getTotalContas() + " contas");
    }
      
      
     
