    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
         try {
            String numero = txtNumeroConta.getText().trim();
            String titular = txtTitular.getText().trim();
            String saldoStr = txtSaldoInicial.getText().trim();
            String tipo = (String) jComboBoxConta.getSelectedItem();
            
            // Validações simples
            if (numero.isEmpty() || titular.isEmpty() || saldoStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
                return;
            }
            
            double saldo = Double.parseDouble(saldoStr);
            Conta conta;
            
            if ("Conta Corrente".equals(tipo)) {
                conta = new ContaCorrente(numero, titular, saldo);
            } else {
                conta = new ContaPoupanca(numero, titular, saldo);
            }
            
            control.adicionarConta(conta);
            JOptionPane.showMessageDialog(this, "Conta cadastrada com sucesso!");
            
            limparCampos();
            atualizarTabela();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Saldo deve ser um número válido!");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

