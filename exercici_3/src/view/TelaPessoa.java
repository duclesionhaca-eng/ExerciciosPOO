     private void jComboBoxTipoActionPerformed(java.awt.event.ActionEvent evt) {                                              
        // Habilita/desabilita campo matrícula baseado no tipo
        String tipo = (String) jComboBoxTipo.getSelectedItem();
        if ("Aluno".equals(tipo)) {
            txtMatricula.setEnabled(true);
            txtMatricula.setBackground(java.awt.Color.WHITE);
        } else {
            txtMatricula.setEnabled(false);
            txtMatricula.setBackground(java.awt.Color.LIGHT_GRAY);
            txtMatricula.setText("");
        }
    } 
    
      private void limparCampos() {
        txtNome.setText("");
        txtMatricula.setText("");
        txtData.setDate(new Date());
        txtNome.requestFocus();
    }

    
