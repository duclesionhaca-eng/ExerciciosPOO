    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       try {
            String nome = txtNome.getText().trim();
            Date dataNascimento = txtData.getDate();
            String tipo = (String) jComboBoxTipo.getSelectedItem();
            
            // Validações
            if (nome.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o nome!");
                return;
            }
            
            if (dataNascimento == null) {
                JOptionPane.showMessageDialog(this, "Selecione a data de nascimento!");
                return;
            }
            
            if ("Aluno".equals(tipo)) {
                String matricula = txtMatricula.getText().trim();
                if (matricula.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Para aluno, preencha a matrícula!");
                    return;
                }
                
                Aluno aluno = new Aluno(nome, dataNascimento, matricula);
                control.cadastrarAluno(aluno);
                JOptionPane.showMessageDialog(this, "Aluno cadastrado com sucesso!");
                
            } else {
                Pessoa pessoa = new Pessoa(nome, dataNascimento);
                control.cadastrarPessoa(pessoa);
                JOptionPane.showMessageDialog(this, "Pessoa cadastrada com sucesso!");
            }
            
            limparCampos();
            atualizarTabela();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

