    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int linhaSelecionada = tbTabela.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma linha para remover!");
            return;
        }
        
        String nome = (String) tbTabela.getValueAt(linhaSelecionada, 1);
        int confirmacao = JOptionPane.showConfirmDialog(this, 
            "Deseja remover: " + nome + "?", 
            "Confirmar Remoção", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirmacao == JOptionPane.YES_OPTION) {
            boolean removido = control.removerPessoa(linhaSelecionada);
            if (removido) {
                JOptionPane.showMessageDialog(this, "Registro removido com sucesso!");
                atualizarTabela();
            }
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

