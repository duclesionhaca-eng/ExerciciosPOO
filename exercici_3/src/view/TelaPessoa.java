    private void configurarComponentes() {
        // Configurar ComboBox
        jComboBoxTipo.removeAllItems();
        jComboBoxTipo.addItem("Pessoa");
        jComboBoxTipo.addItem("Aluno");
        
        // Configurar tabela
        configurarTabela();
        
        // Configurar data para hoje
        txtData.setDate(new Date());
    }

     private void configurarTabela() {
        tableModel = new DefaultTableModel(
            new Object[][]{}, 
            new String[]{"Tipo", "Nome", "Idade", "Matricula"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tbTabela.setModel(tableModel);
    }

     private void atualizarTabela() {
        // Limpa a tabela
        tableModel.setRowCount(0);
        
        // Preenche com os dados usando polimorfismo
        for (Pessoa pessoa : control.getPessoas()) {
            if (pessoa instanceof Aluno) {
                // É um Aluno
                Aluno aluno = (Aluno) pessoa;
                tableModel.addRow(new Object[]{
                    "Aluno",
                    aluno.getNome(),
                    aluno.getIdade(),
                    aluno.getMatricula()
                });
            } else {
                // É uma Pessoa comum
                tableModel.addRow(new Object[]{
                    "Pessoa", 
                    pessoa.getNome(), 
                    pessoa.getIdade(),
                    "---"
                });
            }
        }
    }
     
     

