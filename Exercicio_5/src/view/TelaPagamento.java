 private void configurarComponentes() {
        // Configurar ComboBox
        jComboBox1Funcionario.removeAllItems();
        jComboBox1Funcionario.addItem("Funcionário");
        jComboBox1Funcionario.addItem("Freelancer");
        
        // Configurar tabela
        configurarTabela();
        
        // Configurar labels
        lbTotal.setText("Total de Registros: 0");
        lblTotalPago.setText("Total a Pagar: 0,00 MT");
        lblTotalPago.setForeground(Color.BLUE);
        
        // Configurar campos iniciais
        atualizarCamposPorTipo();
    }
    
     private void configurarTabela() {
        tableModel = new DefaultTableModel(
            new Object[][]{}, 
            new String[]{"Tipo", "Nome", "Detalhes", "Pagamento"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        jTable.setModel(tableModel);
    }
    
     private void atualizarTabela() {
        // Limpa a tabela
        tableModel.setRowCount(0);
        
        // Preenche com os dados usando polimorfismo
        for (Pagavel pagavel : control.getPagaveis()) {
            if (pagavel instanceof Funcionario) {
                Funcionario func = (Funcionario) pagavel;
                tableModel.addRow(new Object[]{
                    "Funcionário",
                    func.getNome(),
                    "Salário Fixo: " + String.format("%.2f", func.getSalarioFixo()) + " MT",
                    String.format("%.2f", func.calcularPagamento()) + " MT"
                });
            } else if (pagavel instanceof Freelancer) {
                Freelancer free = (Freelancer) pagavel;
                tableModel.addRow(new Object[]{
                    "Freelancer", 
                    free.getNome(), 
                    free.getHorasTrabalhadas() + "h @ " + 
                    String.format("%.2f", free.getValorHora()) + " MT/h",
                    String.format("%.2f", free.calcularPagamento()) + " MT"
                });
            }
        }
        
        // Atualiza contador
        lbTotal.setText("Total: " + control.getQuantidadePagaveis() + " registros");
    }
     
      private void atualizarCamposPorTipo() {
        String tipo = (String) jComboBox1Funcionario.getSelectedItem();
        if ("Funcionário".equals(tipo)) {
            jLabel3.setText("Salário Fixo (MT):");
            jLabel5.setText("---");
            jTextField2.setEnabled(false);
            jTextField2.setBackground(Color.LIGHT_GRAY);
            jTextField2.setText("");
            txtValorPorHora.setText("");
        } else {
            jLabel3.setText("Valor por Hora (MT):");
            jLabel5.setText("Horas Trabalhadas:");
            jTextField2.setEnabled(true);
            jTextField2.setBackground(Color.WHITE);
            txtValorPorHora.setText("");
        }
    }
    
     
     
