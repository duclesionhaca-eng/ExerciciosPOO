    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       int linhaSelecionada = jTable.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma linha para eliminar!");
            return;
        }
        
        String nome = (String) jTable.getValueAt(linhaSelecionada, 1);
        int confirmacao = JOptionPane.showConfirmDialog(this, 
            "Deseja eliminar: " + nome + "?", 
            "Confirmar Eliminação", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirmacao == JOptionPane.YES_OPTION) {
            control.getPagaveis().remove(linhaSelecionada);
            control.salvarDados();
            JOptionPane.showMessageDialog(this, "Registro eliminado com sucesso!");
            atualizarTabela();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
         // Usa POLIMORFISMO para calcular todos os pagamentos
        double total = control.calcularTotalPagamentos();
        double totalRecursivo = control.calcularTotalRecursivo(0); // Versão recursiva
        
        int funcionarios = control.contarPorTipo("Funcionário");
        int freelancers = control.contarPorTipo("Freelancer");
        
        lblTotalPago.setText("Total a Pagar: " + String.format("%.2f", total) + " MT");
        
        JOptionPane.showMessageDialog(this,
            "Cálculo realizado com POLIMORFISMO!\n\n" +
            "Funcionários: " + funcionarios + "\n" +
            "Freelancers: " + freelancers + "\n" +
            "Total a pagar: " + String.format("%.2f", total) + " MT\n" +
            "(Recursivo: " + String.format("%.2f", totalRecursivo) + " MT)");
    }//GEN-LAST:event_btnCalcularActionPerformed

