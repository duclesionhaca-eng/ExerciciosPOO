    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

         try {
            String nome = txtNome.getText().trim();
            String tipo = (String) jComboBox1Funcionario.getSelectedItem();
            String valorStr = txtValorPorHora.getText().trim();
            
            // Validações básicas
            if (nome.isEmpty() || valorStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha nome e valor!");
                return;
            }
            
            double valor = Double.parseDouble(valorStr);
            Pagavel pagavel;
            
            if ("Funcionário".equals(tipo)) {
                pagavel = new Funcionario(nome, valor);
            } else {
                String horasStr = jTextField2.getText().trim();
                if (horasStr.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Para freelancer, preencha as horas trabalhadas!");
                    return;
                }
                int horas = Integer.parseInt(horasStr);
                pagavel = new Freelancer(nome, valor, horas);
            }
            
            control.cadastrarPagavel(pagavel);
            JOptionPane.showMessageDialog(this, tipo + " cadastrado com sucesso!");
            
            limparCampos();
            atualizarTabela();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Valores numéricos devem ser números válidos!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

