   private void configurarTabela() {
        tableModel = new DefaultTableModel(
            new Object[][]{}, 
            new String[]{"Nome", "Tipo", "Curso", "Matrícula", "Nota", "Mensalidade", "Status"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        jTable1.setModel(tableModel);
    }
   
    private void atualizarTabela() {
        // Limpa a tabela
        tableModel.setRowCount(0);
        
        // Preenche com os dados dos alunos
        ArrayList<Aluno> alunos = control.getAlunos();
        for (Aluno aluno : alunos) {
            String status = aluno.isAprovado() ? 
                (aluno.isDispensado() ? "✅ Aprovado/Dispensado" : "✅ Aprovado") : 
                "❌ Reprovado";
            
            tableModel.addRow(new Object[]{
                aluno.getNome(),
                aluno.getTipo(),
                aluno.getCurso(),
                aluno.getMatricula(),
                String.format("%.1f", aluno.getNotaFinal()),
                String.format("%.2f MT", aluno.calcularMensalidade()),
                status
            });
        }
        
        // Atualiza estatísticas
        atualizarEstatisticas();
    }
   
     private void atualizarEstatisticas() {
        lblTotalAlunos.setText("Total: " + control.getQuantidadePessoas() + " alunos");
        lblBolseiro.setText("Bolseiros: " + control.contarBolseiros());
        lblNaoBolseiro.setText("Não-Bolseiros: " + control.contarNaoBolseiros());
        lblAprovados.setText("Aprovados: " + control.contarAprovados());
        lblTotalMensalidade.setText("Total Mensalidades: " + 
            String.format("%.2f", control.calcularTotalMensalidades()) + " MT");
    }
    
     
     
