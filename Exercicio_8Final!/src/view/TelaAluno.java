    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            // Validações básicas
            if (txtNome.getText().trim().isEmpty() || 
                txtEmail.getText().trim().isEmpty() ||
                txtMatricula.getText().trim().isEmpty() ||
                txtCurso.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha todos os campos obrigatórios!");
                return;
            }
            
            // Validar data de nascimento
            if (jDateChooserIdade.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Selecione a data de nascimento!");
                return;
            }
            
            // Calcular idade a partir da data de nascimento
            Date dataNascimento = jDateChooserIdade.getDate();
            int idade = calcularIdade(dataNascimento);
            
            // Coleta dados
            String nome = txtNome.getText().trim();
            String email = txtEmail.getText().trim();
            String telefone = txtTelefone.getText().trim();
            String matricula = txtMatricula.getText().trim();
            String curso = txtCurso.getText().trim();
            double mensalidade = Double.parseDouble(txtMensalidade.getText().trim());
            String tipo = (String) jComboBoxTipoAluno.getSelectedItem();
            
            // Cadastra aluno conforme o tipo
            if ("Bolseiro".equals(tipo)) {
                control.adicionarBolseiro(nome, idade, email, telefone, matricula, curso, mensalidade);
            } else {
                control.adicionarNaoBolseiro(nome, idade, email, telefone, matricula, curso, mensalidade);
            }
            
            JOptionPane.showMessageDialog(this, "Aluno cadastrado com sucesso!");
            limparCampos();
            atualizarTabela();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Mensalidade deve ser um número válido!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnActualizarNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarNotaActionPerformed
       int linhaSelecionada = jTable1.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um aluno na tabela!");
            return;
        }
        
        try {
            double nota = Double.parseDouble(txtNotaFinal.getText().trim());
            if (nota < 0 || nota > 20) {
                JOptionPane.showMessageDialog(this, "Nota deve estar entre 0 e 20!");
                return;
            }
            
            boolean sucesso = control.atualizarNota(linhaSelecionada, nota);
            if (sucesso) {
                JOptionPane.showMessageDialog(this, "Nota atualizada com sucesso!");
                atualizarTabela();
                txtNotaFinal.setText("0");
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nota deve ser um número válido!");
        }
    }//GEN-LAST:event_btnActualizarNotaActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         int linhaSelecionada = jTable1.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um aluno para remover!");
            return;
        }
        
        String nome = (String) jTable1.getValueAt(linhaSelecionada, 0);
        int confirmacao = JOptionPane.showConfirmDialog(this, 
            "Deseja remover o aluno: " + nome + "?", 
            "Confirmar Remoção", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirmacao == JOptionPane.YES_OPTION) {
            boolean removido = control.removerPessoa(linhaSelecionada);
            if (removido) {
                JOptionPane.showMessageDialog(this, "Aluno removido com sucesso!");
                atualizarTabela();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

