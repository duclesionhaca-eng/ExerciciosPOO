    private void limparCampos() {
        txtNome.setText("");
        txtEmail.setText("");
        txtTelefone.setText("");
        txtMatricula.setText("");
        txtCurso.setText("");
        txtNotaFinal.setText("0");
        jDateChooserIdade.setDate(null);
        txtNome.requestFocus();
    }

      private int calcularIdade(Date dataNascimento) {
        Date hoje = new Date();
        long diff = hoje.getTime() - dataNascimento.getTime();
        return (int) (diff / (1000L * 60 * 60 * 24 * 365));
    }
    
    
    
