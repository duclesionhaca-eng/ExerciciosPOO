    private void btnGerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGerarActionPerformed
       String tipo = (String) jComboBox1.getSelectedItem();
        
        // Mostra mensagem de processamento
        jTextArea1.setText("üîÑ Gerando relat√≥rio " + tipo + 
                          "Por favor aguarde...");
        
        // Gera o relat√≥rio 
        new Thread(() -> {
            try {
                // Simula um pequeno delay
                Thread.sleep(500);
                
                
                String relatorio = control.gerarRelatorioRecursivo(tipo);
                
               
                SwingUtilities.invokeLater(() -> {
                    jTextArea1.setText(relatorio);
                    JOptionPane.showMessageDialog(this, 
                        "‚úÖ Relat√≥rio gerado com sucesso!\n" +
                        "üìä Tipo: " + tipo + "\n" +
                        "üßÆ M√©todo: RECURSIVIDADE\n" +
                        "üìù Itens processados: " + contarLinhas(relatorio));
                });
                
            } catch (Exception e) {
                SwingUtilities.invokeLater(() -> {
                    JOptionPane.showMessageDialog(this, 
                        "‚ùå Erro ao gerar relat√≥rio: " + e.getMessage());
                });
            }
        }).start();
    }//GEN-LAST:event_btnGerarActionPerformed

    private void btnExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarActionPerformed
           String conteudo = jTextArea1.getText();
        if (conteudo.trim().isEmpty() || conteudo.contains("Selecione o tipo")) {
            JOptionPane.showMessageDialog(this, "Gere um relat√≥rio primeiro!");
            return;
        }
        
        String tipo = (String) jComboBox1.getSelectedItem();
        String nomeArquivo = "relatorio_" + tipo.toLowerCase() + "_" + 
                           new SimpleDateFormat("yyyyMMdd_HHmmss").format(new Date()) + ".txt";
        
        control.exportarRelatorio(tipo, nomeArquivo);
        
        JOptionPane.showMessageDialog(this, 
            "üìÅ Relat√≥rio exportado com sucesso!\n" +
            "üíæ Arquivo: " + nomeArquivo + "\n" +
            "üìä Tipo: " + tipo);
    }//GEN-LAST:event_btnExportarActionPerformed

